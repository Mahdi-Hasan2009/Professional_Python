-- WHICH CUSTOMERS AND  SALESMAN HAS SAME CITIES?
SELECT CUSTOMER.CUST_NAME, SALESMAN.NAME, SALESMAN.CITY 
FROM CUSTOMER
JOIN SALESMAN ON CUSTOMER.CITY = SALESMAN.CITY;

-- LINKING CUSTOMERS TO THEIR SALLESMAN
SELECT CUSTOMER.CUST_NAME, SALESMAN.NAME
FROM CUSTOMER
JOIN SALESMAN ON CUSTOMER.SALESMAN_ID = SALESMAN.SALESMAN_ID;

-- FETCHING ORDERS WHERE CUSTOMER'S CITY DOES NOT MATH SALESMAN CITY
SELECT ORDERS.ORD_NO, CUSTOMER.CUST_NAME, ORDERS.CUSTOMER_ID, ORDERS.SALESMAN_ID
FROM ORDERS
JOIN CUSTOMER ON ORDERS.CUSTOMER_ID = CUSTOMER.CUSTOMER_ID
JOIN SALESMAN ON ORDERS.SALESMAN_ID = SALESMAN.SALESMAN_ID
WHERE CUSTOMER.CITY <> SALESMAN.CITY; -- HERE <> IS NOT EQUAL OPERATOR