# Program to showcase working of different types of bitwise operators

num1 = 10
num2 = 4

# Using AND operator
print("num1 & num2 =", num1 & num2)

# Using OR operator
print("\nnum1 | num2 =", num1 | num2)

# Using NOT operators
print("\n~num1 =", ~num1)

# Using XOR operator
print("\nnum1 ^ num2 =", num1 ^ num2)

num1 = 10
num2 = 4

# Using Left Shift operator on num1
print("\nnum1 << 1 =", num1 << 1)

# Using Right Shift operator on num2 from 2 position
print("\nnum2 >> 2  =", num2  << 2)